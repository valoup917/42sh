long my_add(int a, int b)
{
    return (a + b);
}

long my_sub(int a, int b)
{
    return (a - b);
}

long my_div(int a, int b)
{
    return (a / b);
}

long my_mul(int a, int b)
{
    return (a * b);
}

long my_mod(int a, int b)
{
    return (a % b);
}
